def resolve_paths(dataset):
    ds = dataset.lower()
    # TFNSW lives at the repo root (../tfnsw), not under xxltrafficdata/preprocessed/.
    base = "../tfnsw" if ds == "tfnsw" else f"../xxltrafficdata/preprocessed/{ds}"
    return {
        "raw": f"{base}/RawData/",
        "fast": f"{base}/FastData/",
        "graph": f"{base}/graph/",
    }


def build_groups(grouping, graph_path, year, n_nodes, group_size):
    """Return list of node-index groups for one year (multivariate mode)."""
    if grouping == "index":
        return index_groups(n_nodes, group_size)
    # graph-aware (default)
    adj_fp = os.path.join(graph_path, f"{year}_adj.npz")
    if not os.path.exists(adj_fp):
        print(f"[chronos]   WARN: {adj_fp} missing — falling back to index grouping",
              flush=True)
        return index_groups(n_nodes, group_size)
    adj = np.load(adj_fp)["x"]
    assert adj.shape[0] == n_nodes, \
        f"adj nodes {adj.shape[0]} != data nodes {n_nodes} for {year}"
    return graph_groups(adj, group_size)


def discover_years(raw_path):
    ys = []
    for fn in os.listdir(raw_path):
        if fn.endswith(".npz") and fn[:-4].isdigit():
            ys.append(int(fn[:-4]))
    return sorted(ys)


def append_csv_row(csv_path, row, header):
    new = not os.path.exists(csv_path)
    with open(csv_path, "a", newline="") as f:
        fcntl.flock(f, fcntl.LOCK_EX)
        w = csv.DictWriter(f, fieldnames=header)
        if new:
            w.writeheader()
        w.writerow(row)
        fcntl.flock(f, fcntl.LOCK_UN)


def main():
    ap = argparse.ArgumentParser(description="Chronos-2 zero-shot baseline")
    ap.add_argument("--dataset", required=True, help="e.g. pems03 (case-insensitive)")
    ap.add_argument("--year", type=int, default=None, help="single year; default = all years")
    ap.add_argument("--begin-year", type=int, default=None)
    ap.add_argument("--end-year", type=int, default=None)
    ap.add_argument("--model", default="amazon/chronos-2", help="HF model id")
    ap.add_argument("--gpuid", type=int, default=0)
    ap.add_argument("--device", default=None, help="override, e.g. cpu / cuda:0")
    ap.add_argument("--mode", default="multivariate",
                    choices=["univariate", "multivariate"],
                    help="multivariate = share info across spatially-grouped nodes "
                         "(Chronos-2's headline feature); univariate = per-node")
    ap.add_argument("--grouping", default="graph", choices=["graph", "index"],
                    help="multivariate node grouping: graph (BFS over per-year adj, "
                         "spatially coherent) or index (contiguous chunks)")
    ap.add_argument("--group-size", type=int, default=64,
                    help="variates per multivariate task (Chronos-2 pretrain "
                         "group size is ~100s; large groups are OOD)")
    ap.add_argument("--batch-size", type=int, default=512,
                    help="number of series (variates summed) per Chronos call")
    ap.add_argument("--dtype", default="bfloat16", choices=["float32", "bfloat16", "float16"])
    ap.add_argument("--seed", type=int, default=42)
    ap.add_argument("--out-dir", default="run_logs/chronos")
    ap.add_argument("--no-verify", action="store_true", help="skip FastData reconstruction check")
    ap.add_argument("--max-windows", type=int, default=0,
                    help="DEBUG ONLY: cap test windows per year (0 = use all). "
                         "Non-zero makes numbers NOT comparable; logged loudly.")
    args = ap.parse_args()

    import torch
    torch.manual_seed(args.seed)
    np.random.seed(args.seed)

    if args.device:
        device = args.device
    elif torch.cuda.is_available():
        device = f"cuda:{args.gpuid}"
    else:
        device = "cpu"
    dtype = {"float32": torch.float32, "bfloat16": torch.bfloat16,
             "float16": torch.float16}[args.dtype]

    paths = resolve_paths(args.dataset)
    if not os.path.isdir(paths["raw"]):
        sys.exit(f"[err] raw data path not found: {paths['raw']}")

    if args.year is not None:
        years = [args.year]
    else:
        years = discover_years(paths["raw"])
        if args.begin_year is not None:
            years = [y for y in years if y >= args.begin_year]
        if args.end_year is not None:
            years = [y for y in years if y <= args.end_year]
    if not years:
        sys.exit("[err] no years to run")

    ds_lower = args.dataset.lower()
    out_dir = os.path.join(args.out_dir, ds_lower)
    os.makedirs(out_dir, exist_ok=True)
    csv_path = os.path.join(out_dir, f"{ds_lower}_results.csv")
    csv_header = ["dataset", "year", "model", "mode", "grouping", "group_size",
                  "seed", "n_samples", "n_nodes", "n_groups",
                  "T3_MAE", "T3_RMSE", "T3_MAPE", "T6_MAE", "T6_RMSE", "T6_MAPE",
                  "T12_MAE", "T12_RMSE", "T12_MAPE", "Avg_MAE", "Avg_RMSE",
                  "Avg_MAPE", "secs"]

    grp_desc = (f"mode={args.mode} grouping={args.grouping} group_size={args.group_size}"
                if args.mode == "multivariate" else "mode=univariate")
    print(f"[chronos] dataset={ds_lower} years={years} model={args.model} "
          f"device={device} dtype={args.dtype} bs={args.batch_size} {grp_desc}",
          flush=True)

    print(f"[chronos] loading {args.model} ...", flush=True)
    pipeline = load_pipeline(args.model, device, dtype)
    print("[chronos] model loaded.", flush=True)

    for year in years:
        t0 = time.time()
        test_x, test_y = build_test_windows(
            paths["raw"], year, paths["fast"], verify=not args.no_verify)
        if args.max_windows and test_x.shape[0] > args.max_windows:
            print(f"[chronos] !! DEBUG --max-windows={args.max_windows}: using "
                  f"{args.max_windows}/{test_x.shape[0]} windows — numbers are "
                  f"NOT comparable to the full benchmark.", flush=True)
            test_x = test_x[:args.max_windows]
            test_y = test_y[:args.max_windows]
        S, _, N = test_x.shape
        gt = np.transpose(test_y, (0, 2, 1))  # (S, N, Y_LEN)

        test_x, n_bad = impute_windows(test_x)
        if n_bad:
            print(f"[chronos]   imputed {n_bad}/{S * N} (window,node) contexts "
                  f"with missing values (context-mean fill)", flush=True)

        n_groups = 0
        if args.mode == "multivariate":
            groups = build_groups(args.grouping, paths["graph"], year, N,
                                  args.group_size)
            n_groups = len(groups)
            print(f"[chronos] {ds_lower} {year}: {S} windows x {N} nodes in "
                  f"{n_groups} groups ({args.grouping}, size~{args.group_size}) "
                  f"= {S * n_groups} multivariate tasks", flush=True)
            pred = forecast_multivariate(pipeline, test_x, groups, args.batch_size)
        else:
            print(f"[chronos] {ds_lower} {year}: {S} windows x {N} nodes "
                  f"= {S * N} univariate series", flush=True)
            pred = forecast_univariate(pipeline, test_x, args.batch_size)

        metrics = cal_metric(gt, pred)
        secs = time.time() - t0
        for T in ("3", "6", "12", "Avg"):
            m = metrics[T]
            print(f"[chronos] {ds_lower} {year} T:{T}\tMAE {m['MAE']:.4f}\t"
                  f"RMSE {m['RMSE']:.4f}\tMAPE {m['MAPE']:.4f}", flush=True)
        print(f"[chronos] {ds_lower} {year} done in {secs:.1f}s", flush=True)

        meta = {"dataset": ds_lower, "year": year, "model": args.model,
                "mode": args.mode,
                "grouping": args.grouping if args.mode == "multivariate" else "",
                "group_size": args.group_size if args.mode == "multivariate" else 0,
                "seed": args.seed, "n_samples": int(S), "n_nodes": int(N),
                "n_groups": int(n_groups)}
        rec = dict(meta, secs=round(secs, 1), metrics=metrics)
        tag = args.mode if args.mode == "univariate" else f"mv_{args.grouping}{args.group_size}"
        with open(os.path.join(out_dir, f"{ds_lower}_{year}_{tag}.json"), "w") as f:
            json.dump(rec, f, indent=2)

        row = dict(meta, secs=round(secs, 1))
        for T in ("3", "6", "12", "Avg"):
            row[f"{'T' + T if T != 'Avg' else 'Avg'}_MAE"] = round(metrics[T]["MAE"], 4)
            row[f"{'T' + T if T != 'Avg' else 'Avg'}_RMSE"] = round(metrics[T]["RMSE"], 4)
            row[f"{'T' + T if T != 'Avg' else 'Avg'}_MAPE"] = round(metrics[T]["MAPE"], 4)
        append_csv_row(csv_path, row, csv_header)

    print(f"[chronos] all done. results -> {out_dir}", flush=True)


if __name__ == "__main__":
    main()
